    private void tablaErrores() {
        DefaultTableModel model = (DefaultTableModel) TB_errores.getModel();
        model.setRowCount(0);

        // AÃ±adir cada error a la tabla
        for (Errores error : listaErrores) {
            model.addRow(new Object[]{
                error.getLexema(),
                error.getLinea(),
                error.getColumna(),
                error.getTipo(),
                error.getDesc()
            });
        }
    }

